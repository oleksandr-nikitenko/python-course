a = 17
b = 5

# Addition
print(a + b)

# Subtraction
print(a - b)

# Division
try:
    print(a / b)
except ZeroDivisionError:
    print('Division by zero!')

# Multiplication
print(a * b)

# Exponent
print(a ** b)

# Modulus
try:
    print(a // b)
except ZeroDivisionError:
    print('Division by zero!')

# Floor division
try:
    print(a % b)
except ZeroDivisionError:
    print('Division by zero!')





